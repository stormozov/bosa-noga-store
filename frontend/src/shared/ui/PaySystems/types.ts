/**
 * Тип, представляющий допустимые варианты названий платежных систем.
 */
export type IPaySystemsVariants =
	| "PayPal"
	| "Visa"
	| "MasterCard"
	| "Yandex"
	| "WebMoney"
	| "Qiwi";

/**
 * Интерфейс свойств одной платежной системы.
 *
 * Описывает структуру данных для отображения платежной системы
 * в компоненте `PaySystemItem` и других связанных компонентах.
 */
export interface IPaySystemProps {
	/**
	 * Отображаемое название платежной системы.
	 *
	 * Должно соответствовать одному из значений типа {@link IPaySystemsVariants}.
	 * Используется для стилизации, локализации и генерации атрибутов доступности.
	 *
	 * @example "Visa"
	 */
	displayName: IPaySystemsVariants;

	/**
	 * Ссылка на официальный сайт платежной системы или связанную внешнюю ссылку.
	 *
	 * Если ссылка валидна, компонент будет отображён как кликабельная ссылка.
	 * Может быть пустой строкой, если ссылка отсутствует.
	 *
	 * @example "https://www.visa.com" // будет отображаться тегом `a`
	 * @example "" // будет отображаться тегом `div`
	 */
	link: string;

	/**
	 * Флаг видимости платежной системы.
	 *
	 * Определяет, должна ли платежная система отображаться в интерфейсе.
	 * Позволяет динамически управлять отображением систем без изменения
	 * структуры данных.
	 *
	 * @example
	 * const systems = [{
	 *   displayName: "Visa",
	 *   link: "https://visa.com",
	 *   visible: true
	 * }];
	 *
	 * <PaySystemItem system={systems[0]} />
	 */
	visible: boolean;
}
